export function getCountryCoords(
  countryCode?: string | null,
  countryName?: string | null
): { lat: number; lng: number; name: string } {
  const code =
    (countryCode?.toLowerCase() && COUNTRY_COORDS[countryCode.toLowerCase()]
      ? countryCode.toLowerCase()
      : null) ??
    (countryName && COUNTRY_NAME_TO_CODE[countryName.toLowerCase()]
      ? COUNTRY_NAME_TO_CODE[countryName.toLowerCase()]
      : null)
  const coords = code ? COUNTRY_COORDS[code] : null
  return (
    coords ?? {
      lat: 20,
      lng: 0,
      name: countryName ?? countryCode ?? "不明",
    }
  )
}
